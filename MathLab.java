public class MathLab
{
    public static void main(String[] args)
    {
        int num1 = 4;
        int num2 = 6;
        
        System.out.println("Max: " + Math.max(num1, num2));
        System.out.println("Min: " + Math.min(num1, num2));
        System.out.println("Power: " + Math.pow(num1, num2));
        System.out.println("Log of num1: " + Math.log(num1));
        System.out.println("Log of num2: " + Math.log(num2));
        System.out.println("Hypotenuse: " + Math.hypot(num1, num2));
    }
}

